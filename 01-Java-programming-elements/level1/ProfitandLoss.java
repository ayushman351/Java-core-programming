public class ProfitandLoss {
    public static void main(String[] args) {
        // calculating profit and loss in number and percentage

        int cp=129;
        int sp=191;
        int profit=sp-cp;
        int profitPercentage=(profit/cp)*100;
        System.out.print("The Cost Price is INR "+cp+" and Selling Price is INR "+sp+"\n"+"The Profit is INR "+profit+" and the Profit Percentage is "+profitPercentage);
    }
}
